def division_by_2(number):
    """
    Returns number/2
    :param number: int or float
    :return: int number/2
    """
    return int(number)/2


def multiplication_by_four(number):
    """
    Returns number * 4
    :param number: int or float
    :return: int number * 4
    """
    return int(number)*4


first_result = division_by_2(4)

print(multiplication_by_four(first_result))
